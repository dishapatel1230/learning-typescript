var a:number = 2;
var b:number = 3;

var result;
result = (a & b);
console.log("Bitwise AND : " + result);

result = (a | b);
console.log("Bitwise OR : " + result);

result = (a ^ b);
console.log("Bitwise XOR : " +result);

result = (~b);
console.log("Bitwise NOT: " +result);

result = (a << b);
console.log("Bitwise Left shift :" +result);

result = (a >> b);
console.log("Bitwise Right shift : " +result);

